/**
 * Subtracts two numbers.
 * @param x The first number.
 * @param y The second number.
 * @returns The difference of the two numbers.
 * @example
 * ```typescript
 * // Return 0:
 * sub(1, 1);
 * ```
 * @example
 * ```typescript
 * // Return 2:
 * sub(3, 1);
 * ```
 * @example
 * ```typescript
 * // Return -2:
 * sub(1, 3);
 * ```
 */
export function sub(x: number, y: number): number {
  return x - y;
}
