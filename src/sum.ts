/**
 * Adds two numbers.
 * @param x The first number.
 * @param y The second number.
 * @returns The sum of the two numbers.
 * @example
 * ```typescript
 * // Return 2:
 * add(1, 1);
 * ```
 * @example
 * ```typescript
 * // Return 4:
 * add(3, 1);
 * ```
 */
export function add(x: number, y: number): number {
  return x + y;
}
